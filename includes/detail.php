address and host names of the computers associated with that port.</span></p>
<p>Additionally, the software can display each client’s network card
vendor, port speed, and the counters of erroneous and discarded frames
for every port. This can be of good assistance when troubleshooting
network connectivity issues.</p>
<p><b>Key features:</b><br>
Shows what is connected to every switch port in a simple table format.<br>
Pulls ARP table from a router to display IP information of connected clients.<br>
Scans the current subnet for MAC-to-IP address mappings if there is no router.<br>
Comes in two editions: a GUI application and a console application for using in scripts.<br>
Allows direct launch of other applications and tools for convenience and easy management.<br>
Fully supports SNMPv1, SNMPv2c and SNMPv3, including bulk operations.</p>
